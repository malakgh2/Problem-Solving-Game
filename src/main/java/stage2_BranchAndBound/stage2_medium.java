    public int pick_danger_color(int start , int destination , int nb )
    {
        int couleureInterdite = 11 ,  i=0 ; 
        boolean find = false ;
        while ((i<nb) && (!find))
        {
            if ((probleme.couleur.get(start-1) != probleme.couleur.get(i)) && (probleme.couleur.get(destination-1) != probleme.couleur.get(i)))
            {
                couleureInterdite = i ; 
                find = true ; 
            }
            else 
                i++; 
        }
        return couleureInterdite  ; 
    }
    
