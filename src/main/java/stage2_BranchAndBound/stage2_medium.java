    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:
        try{
         time.stop();
         ArrayList<Integer> cyclePlayer = new ArrayList<>();
         ArrayList<String> cyclePlayers = new ArrayList<>();
         int n = model4.getSize();
         for (int i=0  ; i< n ; i++)
         {
             cyclePlayers.add( model4.get(i).toString()); 
         }
         for(int i =0 ; i<cyclePlayers.size() ; i++)
             cyclePlayer.add(Integer.parseInt(cyclePlayers.get(i)));
         
         /*cyclePlayer.add(1) ; 
         cyclePlayer.add(3) ; 
         cyclePlayer.add(5) ; */
         long costPlayer = probleme.calculate_cost(cyclePlayer) ; 
         long cost = probleme.calculate_cost((ArrayList<Integer>) probleme.finalCycle) ; 
         
         
         if(cyclePlayer.isEmpty())
             jLabel7.setText("Vous etes encore au point de depart!! score : 0");
         else if(cyclePlayer.equals(probleme.finalCycle))
         {
          jLabel7.setText("Brovo , votre circuit est minimal ,  score : 100");
          score = 100 ;
         }
         else if (cyclePlayer.contains(couleureInterdite+1))
            JOptionPane.showMessageDialog(null, "vous avez ete tuer par le poinson du piquet "+ (couleureInterdite+1) +" !! score : 0","Sad for you" , 2);
        
         // else if(cyclePlayer.get(0 )!= probleme.startPiquet)
//                 jLabel2.setText("vous n'avais pas commencé par le point de depart !! score : 0");
         else
         {
             if(cyclePlayer.get(cyclePlayer.size()-1) == destination)
             {  
                 score = 100 - Math.abs((int)(costPlayer - cost) ); 
                 jLabel7.setText("vous atteint la destination , mais pas en fesant le circuit minimal ! score : "+ score);
               
                 
             }
             else if (cyclePlayer.contains(destination))
             {
                 jLabel7.setText("vous avez depassé la destination !! score : 0");
             }
             else 
                 jLabel7.setText("vous n'avais pas atteint la destination !! score : 0");
         }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "vous avez ete tuer par le poinson du piquet "+ (couleureInterdite+1) +" !! score : 0","Sad for you" , 2);
        }
        
        jButton11.setVisible(false);
    }//GEN-LAST:event_jButton11ActionPerformed

